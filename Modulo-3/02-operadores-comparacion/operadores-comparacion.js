// De igualdad simple ==
console.log("hola" == "Hola");
console.log("saludos" == "saludos");
console.log(34 == 34);
console.log(34 == "34");

// De igualdad estricta ===
console.log(12 === 12);
console.log(12 === "12");

// Mayor qué >
console.log(4 > 2);
console.log(4 > 32);
console.log(4 > 4);

// Mayor o igual que >=
console.log(4 >= 2);
console.log(4 >= 4);

// Menor qué <
console.log(5 < 30);
console.log(15 < 2);

// Menor o igual qué <
console.log(30 <= 30);

// Distinto qué simple !=
console.log("hola" != "Hola");
console.log(45 != "45");

// Distinto qué estricto !==
console.log(45 !== "45");
